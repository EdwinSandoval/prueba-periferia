

CREATE TABLE IF NOT EXISTS cliente (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(255),
    email VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS producto (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(255),
    precio DECIMAL(10,2)
);

CREATE TABLE IF NOT EXISTS alumno (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id VARCHAR(20) NOT NULL UNIQUE, -- código alternativo único,
    nombre VARCHAR(255),
    apellido VARCHAR(255),
    estado VARCHAR(10),
    edad INTEGER
);